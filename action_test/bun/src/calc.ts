export class Calc {
  static tambah(a: number, b: number) {
    return a + b;
  }

  static kurang(a: number, b: number) {
    return a - b;
  }

  static kali(a: number, b: number) {
    return a * b;
  }

  static bagi(a: number, b: number) {
    return a / b;
  }

  static mod(a: number, b: number) {
    return a % b;
  }
}
